ColorIndexBar::ColorIndexBar()
{
    _colorindex = "HUE";
    _currentBar = &_barHUE;
}

void ColorIndexBar::setColorIndex(const std::string& colorindex) {
    _colorindex = colorindex;
    
    if (_colorindex == "HUE")
        _currentBar = &_barHUE;
    else if (_colorindex == "NARANJA")
        _currentBar = &_barNARANJA;
    else if (_colorindex == "BLACKFIG")
        _currentBar = &_barBLACKFIG;
}
	
bool ColorIndexBar::getColorIndexBar(std::vector<std::vector<unsigned char>>& bar)
{
	bar.clear();
	if (_colorindex.compare("HUE") == 0)
	{
		for (int i = 0; i < 256; i++)
			bar.push_back(_barHUE[i]);
		return true;
	}

	if (_colorindex.compare("NARANJA") == 0)
	{
		for (int i = 0; i < 256; i++)
			bar.push_back(_barNARANJA[i]);
		return true;
	}

	if (_colorindex.compare("BLACKFIG") == 0)
	{
		for (int i = 0; i < 256; i++)
			bar.push_back(_barBLACKFIG[255-i]);
		return true;
	}

	return false;
}

void ColorIndexBar::getColorValue(std::vector<unsigned char>& color, unsigned char index)
{
    color = (*_currentBar)[_colorindex == "BLACKFIG" ? 255-index : index];
}
